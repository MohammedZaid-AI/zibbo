"""Zibbo coding-assistant benchmark suite.

Reproducible, offline evidence of what Zibbo does to the requests an AI coding assistant
actually sends: READMEs, source files, HTML docs, error logs, JSON responses, PR diffs.
Each dataset case is run through the *real* transformation pipeline in-process — no
provider is contacted, no network is used — and the token/byte reduction, the transformers
applied, and the estimated cost saving are recorded.

"WITHOUT Zibbo" is the original request the provider would have received; "WITH Zibbo" is
the optimized one. Cost is tokens times a published per-model list price (see
``pricing``); it is an estimate, labelled as such, never a bill.

The suite is deterministic in everything that matters — tokens, bytes, transformers — so
its reports can be regenerated byte-for-byte. Wall-clock timing is the one machine-
dependent figure and is reported separately.
"""

